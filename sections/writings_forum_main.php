<h3>Procurement and Dotcoms</h3>

<p>Back in 2000, as the CEO of a then-highly-visible construction-related dotcom (<a target="_blank" href="http://www.heavyware.com">HeavyWare</a>), I
was invited to speak at a forum at the Haas School of Business, University of California: The
Berkeley Forum on Procurement and Marketplace Transformation. . I was not infected with the
boundless enthusiasm and unrealistic expectations of many participants in the dotcom boom. I
had a dotcom because I had already seen that the internet was going to be a big deal, but I also
knew that the complexities of commercial construction were daunting in real life. In speaking
there I felt like a party crasher because I did not speak of how internet magic would rapidly
transform procurement in commercial construction. As I have noticed many times (most recently
in comments on <a target="_blank" href="http://aecbytes.com/blog/?p=316">this AEC Bytes blog post on BIM</a>), software folks are apparently unable to
imagine the complexities embedded in the what and how of commercial construction (there is no
blame here, it took me years to begin to vaguely grasp the full complexity).</p>

<p>There has been much change since this was written. Construction-related dotcoms either
disappeared or morphed into much less ambitious companies. At the same as the dotcom euphoria
was subsiding, an organization was emerging with more realistic approach. It was driven by large
owners. <a target="_blank" href="http://www.fiatech.org/">Fiatech</a> has a goal to  make step change improvement in the design, 
engineering, construction, and maintenance of large capital assets. Their work on procurement is eminently
sensible, focused on foundational work, and in line with my points in this presentation. The scope
of their procurement projects is: Provide the building blocks of standard processes, classification systems, and

data exchange mechanisms to enable fully integrated and automated procurement and supply management systems.
These systems support the seamless execution of procurement functions throughout the selection, delivery, and
payment processes.</p>

<p>In line with my understanding of the critical nature of interfaces in construction, they recognize
that although commonly used materials compose a large percentage of capital projects, the process for selection
and procurement consumes considerable amounts of time and is prone to a variety of errors and oversights. Their
first product-specific project involves valves. Valves are critical in many industries. From my
particular informational perspective valves are a product with known and bounded attributes and
constraints and most important, with well-defined standard interfaces to other elements (pipes,
etc.). In BIM terms, information about valves can be a BIM object that actually knows its
place – knows the rules about interface – and can possibly be part of a model that requires no or
minimal human intelligence to become a set of manufacturing and installation instructions rather

than a statement of “design intent.” This of course is a major step to facilitating procurement.</p>

<p>Nevertheless, the principles I roughly outlined in this presentation have not changed and are
worth considering. Also, keep in mind that overall, A/E/C is more chaotic and complex than E/P/
C and my context is typically A/E/C, because I live there.</p>

